    }

    func loadInitialController() {
        window = UIWindow(frame: UIScreen.main.bounds)
        window?.backgroundColor = .white
        let configurator = TableViewConfiguratorImpl()
        let intialViewController = TableContainerViewController()
        configurator.configure(view: intialViewController)
        let navigationController = UINavigationController(rootViewController: intialViewController)
        self.window?.rootViewController = navigationController
        self.window?.makeKeyAndVisible()
    }
    
